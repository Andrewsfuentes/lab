    private void Buscar_Prod(){
            conexion = null;
            String consulta = "select * from dbo_inventario where articulo like '"+ buscar +"%'";
            Statement s;
            ResultSet rs;
        
        try{
            Class.forName(CONECTOR);
            conexion = (Connection) DriverManager.getConnection(URL, USERNAME, PASSWORD);
            s = conexion.createStatement();
            rs = s.executeQuery(consulta);
                while(rs.next()){
                articulo = rs.getString(2);
                precio = rs.getDouble(4);
                descripcion = rs.getString(5);
                }
                lbl_DataProd.setText("Articulo: "+articulo);
                
            conexion.close();
        }
        
        catch (ClassNotFoundException | SQLException e){
            lbl_DataProd.setText("Error de conexi√≥n "+ e);
        }
    }

